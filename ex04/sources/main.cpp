{
	if (argc != 4)
	{
		std::cerr << "Usage: " << argv[0] << " <filename> <string to replace> <replacement string>" << std::endl;
		return (1);
	}

	std::string	filename(argv[1]);
	std::string	s1(argv[2]);
	std::string	s2(argv[3]);
	stringReplacer(filename, s1, s2);
	return (0);
}
