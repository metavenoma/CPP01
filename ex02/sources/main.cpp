#include <iostream>

int	main(void)
{
	std::string brain = "HI THIS IS BRAIN";
	std::string* stringPTR = &brain;
	std::string& stringREF = brain;

	std::cout << "memory address of the string variable: " << &brain << std::endl;
	std::cout << "memory address held by string pointer: " << stringPTR << std::endl;
	std::cout << "memory address held by string reference: " << &stringREF << std::endl;
	std::cout << "value of string variable: " << brain << std::endl;
	std::cout << "value pointed to by pointer: " << *stringPTR << std::endl;
	std::cout << "value pointed to by stringREF: " << stringREF << std::endl;
}
